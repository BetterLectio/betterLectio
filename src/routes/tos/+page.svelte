<h2 class="text-2xl font-bold">2. Servicevilkår</h2>
<p class="my-4">
  Vi påtager os ikke ansvar for hændelser eller skader, der måtte forekomme i forbindelse med din brug af vores tjenester. Det er vigtigt at
  forstå, at vi ikke kan drages til ansvar for eventuelle forhindringer eller problemer, du oplever under brugen af vores tjenester. Mens vi
  stræber efter nøjagtighed, kan vi ikke holdes ansvarlige for unøjagtigheder i oplysningerne på vores hjemmeside. Ligeledes kan vi ikke
  tage ansvaret for situationer eller begivenheder, der opstår under din brug af vores tjenester, eller som opstår som en følge heraf. Det
  er også vigtigt at anerkende, at vi ikke er ansvarlige for konsekvenserne af tidligere brug af vores tjenester. I forlængelse heraf kan vi
  ikke holdes ansvarlige for begrænset adgang til visse funktioner på vores hjemmeside. Vi har retten til at ændre vores servicevilkår og
  privatlivspolitik uden varsel, så det er din pligt at følge med i eventuelle opdateringer.
</p>

<h2 class="text-2xl font-bold">3. Privatlivspolitik</h2>
<p class="my-4">
  Under din login-proces gemmer din browser en Base64-kodet version af oplysninger såsom brugernavn og skole-ID. Disse oplysninger er
  nødvendige for at hente data fra Lectio. Efterfølgende sender vi disse oplysninger til vores servere, som videreformidler dem til Lectio.
  Disse data anvendes ikke til noget andet formål hos os, og de opbevares ikke på vores servere. Din log ind-proces lettes gennem denne
  metode, men det er vigtigt at bemærke, at vi hverken har adgang til eller synlighed af disse oplysninger på vores servere. Ikke desto
  mindre er vi forpligtede til at opbevare dit Lectio-bruger-ID, skole-ID samt eventuelle roller, tilknyttede konti og pro-status. Disse
  data gemmes i vores database. Vær opmærksom på, at vi ikke kan garantere, at vores hosting-udbydere ikke logger din aktivitet på vores
  hjemmeside.
</p>

